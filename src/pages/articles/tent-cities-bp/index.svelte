  <p class="intro-p"> <br/>In Charlotte, North Carolina, the chasm between the rich and the poor is so wide, the city has historically been coined a “tale of two cities:” one of prosperity and the other of poverty. 
     <br/><br/>
     The most visible effect of this social stratification is the <strong>“Tent City,”</strong> a cluster of homeless encampments located at the northern end of Uptown 
     on North College, West 12th, and North Tryon streets.<br/>
     <br/>
